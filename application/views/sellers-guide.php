<span class="fontbld">Where can I monitor all my auction activity?</span></br></br>
You can monitor your auction activity by logging into using your login and password. You can
view the bidding history of the live auctions, reserve prices (Reserve Price can only be viewed
by Seller and no one else), and other features. However you will be auto emailed once the
auction ends with detail information on the auction (Highest Bidder, Subject to Approval items,
Rejected Items).</br></br>

<span class="fontbld">Can I edit my auction after it starts?</span></br></br>
You will not be allowed to edit any item once the auction starts.</br></br>

<span class="fontbld">How do I cancel bids in my auction?</span></br></br>

You cannot cancel a bid. But you can restrict a buyer from bidding. For that you have to contact
Shopemet Networks Pvt Ltd and make a formal request to lock the specific buyer&#39;s bidding.</br></br>

<span class="fontbld">How do I close or cancel my auction?</span></br></br>
You cannot close / cancel your auction once it starts. You can cancel your auction prior to the
starting of the auction. For that you have to contact Shopemet Networks Pvt Ltd and make a
formal request to cancel the auction.</br></br>

<span class="fontbld">How do I answer a bidder&#39;s question?</span></br></br>
Bidder&#39;s question that will come to you by email can be answered by clicking the &#39;Reply&#39; button
