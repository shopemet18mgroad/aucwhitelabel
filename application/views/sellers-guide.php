<span class="fontbld">What if my Reserve Price is not met?</span></br></br>
If the reserve price has not been met by close of auction, the auction closes without a winning
bidder. If the bid price matches the limits specified for inclusion in Subject To Approval (STA),
then the same shall be shown under STA category and you will be informed about that. In this
case you have to convey the acceptability of the bid amount or otherwise of the bid value to
AucJunction as well as the bidder within 3 days (Excluding holidays) of the close of the auction.
On your request AucJunction will arrange for inclusion of the unsold item in your next auction.</br></br>

<span class="fontbld">How do I rate a bidder?</span></br></br>
There are various reports available by which you can rate a bidder.</br></br>

<span class="fontbld">I received a complaint against my auction. What does this mean?</span></br></br>
